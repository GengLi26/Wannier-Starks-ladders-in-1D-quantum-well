% plot eigen energis (trivial states removed) at different E field. 
% F for different columne 
% Lable of different column of excell file. 
E_Band=[0,2:1:10,12:2:30,40,50];

count_color=1;
set_count=[2:2:10,15,20];

for count=set_count
    
    color_plot=[1 1 1].*0.8-0.6*count_color/length(set_count);
    count_color=count_color+1;
    scatter([0:1:30],Band(5:1:35,count)-min(Band(5:1:35,count)),100,color_plot)
    hold on
    
    F_field_fit=E_Band(count)
    Period_fit=7e-9;
    deltaE=F_field_fit.*1e5.*Period_fit;
    
    E=[0:1:39].*deltaE;
    plot([0:1:39],E,'color',color_plot,'linewidth',2)
    hold on 
end 

